  CREATE OR REPLACE FUNCTION fun_pate_solicitud_pagos()
    RETURNS TRIGGER AS $$
    BEGIN
      NEW.sopa_fechamod = NOW();

      IF NEW.sopa_estado = 'RADICADO' AND OLD.sopa_fecha_radicado IS NULL THEN
        NEW.sopa_fecha_radicado = NOW();
      END IF;

      RETURN NEW;
    END;
  $$ LANGUAGE plpgsql;

